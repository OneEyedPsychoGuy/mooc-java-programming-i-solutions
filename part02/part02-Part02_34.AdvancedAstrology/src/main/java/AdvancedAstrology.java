public class AdvancedAstrology {
    public static void main(String[] args) {

    }

    public static void printStars(int number) {
        while(number > 0) {
            System.out.print("*");
            number--;
        }
        System.out.println("");
    }

    public static void printSpaces(int number) {
        while(number > 0) {
            System.out.print(" ");
            number--;
        }
    }
}